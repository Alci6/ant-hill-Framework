  <h2>Backlog</h2>
  <section class="cf pa3 mt0 pt0 fw3 mt0">
    <div class="fl w-100 w-100-m w-75-l f3-l f4-ns ">
      <p>
        El Backlog es un listado ordenado de necesidades que deben ser cubiertas
        para satisfacer a un usuario.
      </p>

      <p>
        Puede estar compuesto por épicas, Historias de Usuario, Spikes, Tickets
        o cualquier otra técnica o medio que permita al equipo establecer un
        <mark>entendimiento común y compartido</mark>
        sobre lo que debe contener un producto o entregarse en un servicio.
      </p>
      <p>
